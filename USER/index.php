<h1 style="text-align: center;" class="text-primary;" >APA ITU UMKM ? </h1>
<br>

<h5 style="text-align: center;">Pengertian UMKM melalui UU No.9 Tahun 1999 dan karena keadaan perkembangan yang semakin dinamis dirubah ke Undang-Undang No.20 Pasal 1 Tahun 2008 tentang Usaha Mikro, Kecil dan Menengah maka pengertian UMKM adalah sebagai berikut: 

<h5 style="text-align: center;">1)	Usaha Mikro adalah usaha produktif milik orang perorangan dan/atau badan usaha perorangan yang memenuhi kriteria Usaha Mikro sebagaimana diatur dalam Undang- Undang ini.</h5>
<h5 style="text-align: center;">2)	Usaha Kecil adalah usaha ekonomi produktif yang berdiri sendiri, yang dilakukan oleh orang perorangan atau badan usaha yang bukan merupakan anak perusahaan atau bukan cabang perusahaan yang dimiliki, dikuasai, atau menjadi bagian baik langsung maupun tidak langsung dari Usaha Menengah atau Usaha Besar yang memenuhi kriteria Usaha Kecil sebagaimana dimaksud dalam Undang-Undang ini.</h5>
<h5 style="text-align: center;">3)	Usaha Menengah adalah usaha ekonomi produktif yang berdiri sendiri, yang dilakukan oleh orang perorangan atau badan usaha yang bukan merupakan anak perusahaan atau cabang perusahaan yang dimiliki, dikuasai, atau menjadi bagian baik langsung maupun tidak langsung dengan Usaha Kecil atau Usaha Besar dengan jumlah kekayaan bersih atau hasil penjualan tahunan sebagaimana diatur dalam Undang- Undang ini
</h5></h5>
<br>
<br>
<h4 style="text-align: center; ">JENIS-JENIS UMKM</h4>
<h5 style="text-align: center;">Usaha kuliner adalah UMKM yang bergerak dalam bidang kuliner seperti menjual makanan maupun bahan baku pembuatan makanan
</h5>
<h5 style="text-align: center;">Usaha fashion adalah UMKM yang bergerak dibidang fashion melingkupi penjualan dan pembuatan pakaian, alas kaki, topi, hingga aksesoris.
</h5>
<h5 style="text-align: center;">UMKM bidang usaha agribisnis meliputi penjualan dan produksi pertanian serta perkebunan seperti pupuk, hasil tani, hasil kebun, dan bibit tanaman
</h5>
<h5 style="text-align: center;">UMKM bidang usaha Penjualan Pulsa dan Kuota Serta Top Up berbagai aplikasi Seperti Dana, Gojek, dan Ovo.
</h5>
<br>
<br>
<h5 style="text-align: center;">UMKM DESA SITANGGAL</h5>
<br>
<h5 style="text-align: center;">Semakin hari Banyak UMKM yang berada di Desa Sitanggal tentunya ini membuat daya saing yang semakin tinggi dengan mengelola usaha kita dengan baik akan dapat meningkatkan peluang usaha kedepan menjadi lebih baik , UMKM di Desa Sitanggal banyak sekali jenisnya seperti menu makanan, minuman, dan produk pulsa kuota dan Top Up aplikasi .Diharapkan dengan adanya aplikasi ini masyarakat Desa Sitanggal dan sekitarnya dapat dengan mudah dalam mencari produk yang di inginkan dan juga para pelaku UMKM yang mendapatkan keuntungan dalam mempromosikan produknya dengan jaringan yang lebih luas </h5>
